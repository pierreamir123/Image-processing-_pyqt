    def freq_unsharp_mask(self,not_padded_image,kernal):
        padded_kernal,padded_image = self.filter_class.kernal_padding(not_padded_image,kernal)
        fft_lowpass = np.multiply(np.fft.fftshift(np.fft.fft2(padded_kernal)),np.fft.fftshift(np.fft.fft2(padded_image)))
        output_lowpass = np.fft.ifft2(np.fft.ifftshift(fft_lowpass)).real
        output_lowpass = np.fft.ifftshift(output_lowpass)
        image_filter = padded_image - output_lowpass
        nj = (image_filter - image_filter.min()) * 255
        N = image_filter.max() - image_filter.min()
        scaled = nj / N
        return (scaled,output_lowpass)

    
