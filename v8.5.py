            output_highpass = np.fft.fftshift(np.fft.ifft2(np.fft.ifftshift(fft_highpass)).real)
            output_highpas = np.multiply(output_highpass,self.f_combo_factor_2.value())
            image_filter = self.image_process.image_o + output_highpas
            nj = (image_filter - image_filter.min()) * 255
            N = image_filter.max() - image_filter.min()
            scaled = nj / N
            self.freq_filter_fig.ax2.imshow(scaled.astype('uint8'),cmap='gray',vmax=255 , vmin = 0)
            self.freq_filter_fig.ax1.imshow(output_highpass.astype('uint8'),cmap='gray',vmax=255 , vmin = 0)
            self.freq_filter_fig.draw()
        
        
